<h2>Politique de confidentialité</h2>
<br/>
<p>
<br/>Protection de vos données à caractère personnel<br/>
<br/>Dans le cadre de l’utilisation de ses services et notamment des services accessibles sur son site internet ou ses applications mobiles, AIR FRANCE, en qualité de responsable de traitement, collecte et traite des données à caractère personnel vous concernant.
<br/>
<br/>La présente politique de confidentialité ("Politique") s’applique à toutes les données que nous traitons notamment lorsque vous effectuez une réservation, un achat de billet, voyagez avec nous, achetez ou utilisez l’ensemble de nos services, visitez notre site internet, utilisez nos applications mobiles ou lorsque vous interagissez avec nous par les différents canaux mis à votre disposition.
<br/>
<br/>Il est important que vous preniez connaissance de cette Politique, ainsi que toute autre politique relative à la confidentialité que nous pourrions être amenés à vous soumettre – comme notre Politique de confidentialité Flying Blue si vous êtes membre de notre programme de fidélité – afin que vous soyez pleinement informé des traitements de données à caractère personnel vous concernant, des moyens mis en œuvre pour les protéger ainsi que des modalités selon lesquelles vous pouvez exercer vos droits.
<br/>
<br/>La présente Politique n’est pas un contrat et n’engendre aucune obligation contractuelle.
</p>
