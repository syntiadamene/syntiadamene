	<h2> Déclaration d'accessibilité </h2>
	<br/>Air France s'engage à garantir à ses clients la meilleure expérience de voyage, en commençant par un site et une application accessibles et de qualité. Découvrez nos engagements en matière d’accessibilité.
	<br/>
	<br/>Nos engagements
	<br/>Nous nous engageons à rendre notre site et application accessible à tous, quel que soit le handicap. Pour ce faire, nous suivons les règles pour l’accessibilité des contenus web ,critère de niveau AA de la Web Accessibility Initiative .
	<br/>
	<br/>Ces règles nous permettent de créer des expériences digitales accessibles sur notre site et appli pour tous les passagers, avec ou sans handicap.
	<br/>Des commentaires liés à l'accessibilité ?
	<br/>Nous nous efforçons d'atteindre nos objectifs d'accessibilité, mais sommes conscients que certains domaines doivent encore être améliorés. Avez-vous rencontré des difficultés sur notre site ou application ? Avez-vous des idées sur comment nous pourrions nous améliorer ? N'hésitez pas à nous contacter.

	<br/>
	<br/>Les voyages de tous nos passagers doivent se dérouler dans les meilleures conditions. Si vous avez besoin d'assistance à l'aéroport ou pendant votre vol, nos équipes sont là pour s'assurer que vous voyagez avec tout ce dont vous avez besoin pour garantir un voyage en toute tranquillité.
